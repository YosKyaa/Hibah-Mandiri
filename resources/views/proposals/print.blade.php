Berdasarkan hasil seleksi proposal dari Tim Reviewer Internal JGU menetapkan penerima hibah internal penelitian yang
ditetapkan melalui Surat Keputusan Rektor Universitas Global Jakarta Nomor : 91/SK/JGU/VI/2024 tanggal 08 Juni 2024
tentang Penerima Program Hibah Internal Penelitian Tahun 2024, maka bersama ini kami sampaikan daftar nama terlampir.
Berkenaan dengan hal tersebut, Research Management Centre mengucapkan selamat kepada para Penerima Program Hibah
Internal Penelitian Tahun 2024. Research Management Centre mengucapkan terima kasih kepada pengusul yang telah
berpartisipasi dan apabila nama pengusul tidak tercantum dalam lampiran, maka dapat mengusulkan kembali pada tahun yang
akan datang.
Berkaitan dengan hal ini, dapat kami sampaikan beberapa hal sebagai berikut yaitu:
1. Bagi pengusul yang dinyatakan lolos dan menerima pendanaan hibah untuk dapat segera merevisi proposal hasil review
sebagaimana yang tertulis dalam catatan review. Proposal revisi dapat diupload di www.rmc.jgu.ac.id. Mulai tanggal 10
s/d 16 Juni 2024 pukul 16.00 WIB. Jika pada sampai batas waktu pengumpulan proposal revisi, calon penerima hibah belum
mengumpulkan maka pengusul akan dinyatakan gugur.
2. Penandatanganan kontrak pelaksanaan program penelitian akan diinformasikan lebih lanjut.
3. Hal-hal lain yang terkait dengan penandatanganan kontrak, pencairan dana dan pelaksanaan program penelitian akan
diinformasikan lebih lanjut melalui web RMC (www.rmc.jgu.ac.id).
Demikian surat ini kami buat, atas perhatian dan kerjasamanya diucapkan terima kasih.


